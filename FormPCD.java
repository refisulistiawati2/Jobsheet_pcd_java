void drawHistogram(BufferedImage test, String judul)
{
    int width = test.getWidth();
    int height = test.getHeight();

    int count[][] = new int[4][256];

    for(int i=0;i<width;i++)
    {
        for(int j=0;j<height;j++)
        {
            int color = test.getRGB(i,j);

            int red = (color & 0x00ff0000)>>16;
            int green = (color & 0x0000ff00)>>8;
            int blue = color & 0x000000ff;

            int gray = (red + green + blue)/3;

            count[0][red]++;
            count[1][green]++;
            count[2][blue]++;
            count[3][gray]++;
        }
    }

    DefaultCategoryDataset dataset =
            new DefaultCategoryDataset();

    for(int i=0;i<256;i++)
    {
        dataset.addValue(count[0][i], "Red", String.valueOf(i));
        dataset.addValue(count[1][i], "Green", String.valueOf(i));
        dataset.addValue(count[2][i], "Blue", String.valueOf(i));
        dataset.addValue(count[3][i], "Gray", String.valueOf(i));
    }

    JFreeChart chart =
            ChartFactory.createBarChart(
                    "Histogram",
                    "Pixel",
                    "Frekuensi",
                    dataset);

    ChartFrame frame =
            new ChartFrame(judul, chart);

    frame.setSize(800,500);
    frame.setVisible(true); // TODO add your handling code here:
    }//GEN-LAST:event_jButton10ActionPerformed

