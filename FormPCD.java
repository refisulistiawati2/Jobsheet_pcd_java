    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
         image = new ImageIcon(sumberGambar).getImage();

    size = new Dimension();
    size.width = image.getWidth(null);
    size.height = image.getHeight(null);
    setPreferredSize(size);

    int Sn = 0;
    int Ss = 0;
    int Sn2 = 0;
    int avg = 0;

    prosesimage = new BufferedImage(
            size.width,
            size.height,
            BufferedImage.TYPE_INT_RGB);

    prosesimage2 = new BufferedImage(
            size.width,
            size.height,
            BufferedImage.TYPE_INT_RGB);

    prosesimage3 = new BufferedImage(
            size.width,
            size.height,
            BufferedImage.TYPE_INT_RGB);

    Graphics g = prosesimage.getGraphics();
    g.drawImage(image, 0, 0, null);

    // Membuat citra noisy
    for (int x = 0; x < size.width; x++) {
        for (int y = 0; y < size.height; y++) {

            int RGB = prosesimage.getRGB(x, y);

            int alpha = (RGB << 24) & 0xFF;
            int red = (RGB >> 16) & 0xFF;
            int green = (RGB >> 8) & 0xFF;
            int blue = RGB & 0xFF;

            avg = (red + green + blue) / 3;

            double r = Math.random();
            double p = Double.parseDouble(jTextField4.getText());

            int avg2 = (int)(avg + r * 256 * p);

            if (avg2 > 255) {
                avg2 = 255;
            }

            int gray =
                    alpha |
                    avg2 << 16 |
                    avg2 << 8 |
                    avg2;

            prosesimage2.setRGB(x, y, gray);

            Sn = Sn + Math.abs(avg2 - avg);
            Ss = Ss + Math.abs(avg);
        }
    }

    // Tampilkan gambar noise
    lblGambar2.setIcon(new ImageIcon(prosesimage2));

    double snr = 10 * Math.log10((double) Ss / Sn);
    jTextField5.setText(Double.toString(snr));

    // Filter rata-rata 5x5
    for (int v = 2; v <= size.height - 3; v++) {
        for (int u = 2; u <= size.width - 3; u++) {

            int sum = 0;
            int temp = 0;

            for (int j = -2; j <= 2; j++) {
                for (int i = -2; i <= 2; i++) {

                    int RGB = prosesimage2.getRGB(u + i, v + j);

                    int alpha = (RGB << 24) & 0xFF;
                    int red = (RGB >> 16) & 0xFF;

                    temp = alpha;
                    sum = sum + red;
                }
            }

            int q = (int)Math.round(sum / 25.0);

            int gray2 =
                    temp |
                    q << 16 |
                    q << 8 |
                    q;

            prosesimage3.setRGB(u, v, gray2);

            Sn2 = Sn2 + Math.abs(q - avg);
        }
    }

    // Tampilkan hasil reduksi noise
    lblGambar3.setIcon(new ImageIcon(prosesimage3));

    double snr2 = 10 * Math.log10((double) Ss / Sn2);
    jTextField6.setText(Double.toString(snr2));   
    }//GEN-LAST:event_jMenuItem6ActionPerformed

